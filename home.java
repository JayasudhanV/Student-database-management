    public boolean isEmptyStudent(){
        if(jTextField2.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"student name is missing");
            return false;
        }
         if(jTextField9.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Age  is missing");
            return false;
        }
         if(jTextField3.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"email  is missing");
            return false;
        }
         if(!jTextField3.getText().matches("^.+@.+\\..+$")){
            JOptionPane.showMessageDialog(this,"invalid mail id");
            return false;
        }
         if(jTextField4.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"phone NO  is missing");
            return false;
        }
         if(jTextField4.getText().length()>=15){
            JOptionPane.showMessageDialog(this,"not a vaild No");
            return false;
        }
         if(jTextField5.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"father's name is missing");
            return false;
        }
         if(jTextField6.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Mother's name is missing");
            return false;
        }
         if(jTextField7.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"address line 1 is missing");
            return false;
        }
         if(jTextField8.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"address line 2 is missing");
            return false;
        }
         return true;
    }
