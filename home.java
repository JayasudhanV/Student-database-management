    private void jButton4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton4MouseClicked
        
    // Check if any of the text fields are empty
    if (jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty() || 
        jTextField4.getText().isEmpty() || jTextField5.getText().isEmpty() || 
        jTextField6.getText().isEmpty() || jTextField7.getText().isEmpty() || 
        jTextField8.getText().isEmpty() || jTextField9.getText().isEmpty()) {
        
        JOptionPane.showMessageDialog(this, "Missing Information");
    } else {
        Connection Con = null;
        PreparedStatement Add = null;

        try {
            // Establish a connection to the database
            Con = DriverManager.getConnection("jdbc:mysql://localhost:3306/students_management", "root", "STDPRO1");

            // Prepare the SQL statement with placeholders for parameters
            String sql = "INSERT INTO PassengersTbl (name, age, gender, phone, father, mother, address1, address2) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
            Add = Con.prepareStatement(sql);
            
            // Set the values for the parameters in the SQL query
            Add.setString(1, jTextField2.getText()); // name
            Add.setString(2, jTextField9.getText()); // age
            Add.setString(3, jTextField3.getText()); // gender
            Add.setString(4, jTextField4.getText()); // phone
            Add.setString(5, jTextField5.getText()); // father
            Add.setString(6, jTextField6.getText()); // mother
            Add.setString(7, jTextField7.getText()); // address1
            Add.setString(8, jTextField8.getText()); // address2
            
            // Execute the update and confirm the addition
            int row = Add.executeUpdate();
            if (row > 0) {
                JOptionPane.showMessageDialog(this, "Passenger Added");
            }

        } catch (Exception ex) {
            ex.printStackTrace();
        } finally {
            // Close resources in the finally block
            try {
                if (Add != null) Add.close();
                if (Con != null) Con.close();
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }


        
    }//GEN-LAST:event_jButton4MouseClicked
            
